class PropertyModel {
  constructor(
    id,
    owner,
    price,
    state,
    city,
    address,
    type,
    image_name,
    image_id,
    image_url,
    status
  ) {
    this.id = id;
    this.owner = owner;
    this.price = price;
    this.state = state;
    this.city = city;
    this.address = address;
    this.type = type;
    this.image_name = image_name;
    this.image_id = image_id;
    this.image_url = image_url;
    this.status = status;
  }
}
